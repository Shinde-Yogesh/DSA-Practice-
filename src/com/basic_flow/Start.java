package com.basic_flow;

public class Start {
	public static void main(String[] args) {
		System.out.println("Program is running ");
		
		// If  Else Loop
		int time = 22;
		if (time < 10) {
		  System.out.println("Good morning.");
		} else if (time < 18) {
		  System.out.println("Good day.");
		} else {
		  System.out.println("Good evening.");
		}
	}

}
